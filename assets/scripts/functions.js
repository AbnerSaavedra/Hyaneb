function pintarEficacia(){
    
    let color = "#004587";
    let elements;

    console.log("Aquí");
    elements = document.getElementsByClassName("eficacia");
    elements[0].style.backgroundColor = color;
    var URLactual = window.location.pathname;
    alert(URLactual);
}

